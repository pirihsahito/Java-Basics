public class Loops {
    public static void main(String[] args) {
        System.out.println("For Loop in Java :");

        // Example 1
        for(int d = 1; d<=15; d++){
            System.out.println(d);
        }

        //  Example 2
        int n = 5;
        for(int e = 0; e<n; e++){
            System.out.println(2*e+1);
        }

        //  Example 3
        for(int f = 10; f>0; f--){
            System.out.println(f);
        }
    }
}
